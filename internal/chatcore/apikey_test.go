// setupTestDB opens an in-memory SQLite database and migrates the
// ApiKey model. It returns the handle and a teardown helper that the
// caller can ignore (the DB lives only for the test process).
func setupTestDB(t *testing.T) (*gorm.DB, func()) {
	t.Helper()
	db, err := gorm.Open(sqlite.Open(":memory:"))
	if err != nil {
		t.Fatalf("open sqlite: %v", err)
	}
	if err := db.AutoMigrate(&model.ApiKey{}); err != nil {
		t.Fatalf("auto migrate: %v", err)
	}
	return db, func() { _ = db }
}

// mustInsertKey is a test helper that inserts an ApiKey row and fails
// the test on error. The id field is generated by the caller; the rest
// of the struct is supplied as arguments.
func mustInsertKey(t *testing.T, db *gorm.DB, id, key string, active *bool) {
	t.Helper()
	k := model.ApiKey{ID: id, Key: key, IsActive: active}
	if err := db.Create(&k).Error; err != nil {
		t.Fatalf("insert api key %q: %v", key, err)
	}
}

// TestAPIKeyValidator_ValidKey exercises AC-001: a key that exists and
// is active passes when requireApiKey is true.
func TestAPIKeyValidator_ValidKey(t *testing.T) {
	db, teardown := setupTestDB(t)
	defer teardown()
	mustInsertKey(t, db, "1", "sk-valid", boolPtr(true))

	v := newAPIKeyValidator(db)
	if err := v.Validate(true, "sk-valid"); err != nil {
		t.Errorf("valid key rejected: %v", err)
	}
}

// TestAPIKeyValidator_MissingKey exercises AC-002: empty key with
// requireApiKey=true is rejected as "Missing API key".
func TestAPIKeyValidator_MissingKey(t *testing.T) {
	db, teardown := setupTestDB(t)
	defer teardown()

	v := newAPIKeyValidator(db)
	err := v.Validate(true, "")
	if err == nil {
		t.Fatal("expected error for missing key, got nil")
	}
	if !errors.Is(err, errAPIKeyRejected) {
		t.Errorf("error = %v, want errAPIKeyRejected", err)
	}
	if err.Error() != "api key rejected: Missing API key" {
		t.Errorf("message = %q, want %q", err.Error(), "api key rejected: Missing API key")
	}
}

