// TestValidateAPIKey_HandlerIntegration exercises the full HTTP handler
// path: when the validator rejects, ChatCompletionsHandler must return
// 401 with the OpenAI error envelope.
func TestValidateAPIKey_HandlerIntegration(t *testing.T) {
	db, teardown := setupTestDB(t)
	defer teardown()

	v := newAPIKeyValidator(db)
	err := v.Validate(true, "sk-invalid")
	if err == nil {
		t.Fatal("expected validation error")
	}

	// Verify the error message matches what WriteError would emit.
	expectedMsg := "Invalid API key"
	if err.Error() != "api key rejected: Invalid API key" {
		t.Errorf("error message mismatch: got %q, want ending %q", err.Error(), expectedMsg)
	}

	// WriteError should produce 401 with the OpenAI envelope.
	w := httptest.NewRecorder()
	WriteError(w, http.StatusUnauthorized, expectedMsg)
	resp := w.Result()
	if resp.StatusCode != http.StatusUnauthorized {
		t.Errorf("status = %d, want %d", resp.StatusCode, http.StatusUnauthorized)
	}
}

// boolPtr is a small test helper that returns a *bool pointing to the
